package workshop.task4.v2.utils;

public class Package {

  public String message;
  public String from;
  public String to;
  public String status;

  public Package() {
  }

  public Package(String message, String from, String to, String status) {
    this.message = message;
    this.from = from;
    this.to = to;
    this.status = status;
  }

}
